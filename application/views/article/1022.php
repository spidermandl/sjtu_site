                <h2>上海交通大学ACM代表队勇夺第40届ACM国际大学生程序设计竞赛总决赛金牌</h2>

				<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在2016年5月19日刚刚结束的第40届ACM国际大学生程序设计竞赛全球总决赛上，由致远学院计算机方向、电院计算机专业学生倪昊斌、刘严培、黄文瀚组成的上海交通大学代表队，在我系俞勇教授的指导下，获得世界亚军，勇夺总决赛金牌。这也是我系在ACM大赛上收获的第六枚金牌（2002、2005、2010年世界冠军，2013、2016年世界亚军，2012年全球第四）！圣彼得堡国立大学和哈佛大学代表队分获冠军和季军，同得金牌。
				</p>
				<br/>
 				<p>
					<img src="../img/1022/1.jpg" style="height:70% !important;width:70% !important;" />
				</p>
				<br/>

				<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本次比赛在泰国宋卡王子大学举行，全球共有128支队伍参赛，其中包括哈佛大学、麻省理工大学、斯坦福大学等世界一流大学，竞争异常激烈。
				比赛中，上海交大代表队与圣彼得堡国立大学代表队均解出11道题目，但最终以7分钟的微弱罚时劣势惜败于圣彼得堡国立大学，排名第二，获得亚军。
				</p>
				<br/>
 				<p>
					<img src="../img/1022/2.jpg" style="height:70% !important;width:70% !important;" />
				</p>
				<br/>

				<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;中国大陆地区共有17支代表队晋级总决赛，除了上海交通大学，还有清华大学、北京大学、浙江大学、复旦大学、北京航空航天大学、天津大学等。其中，复旦大学排名12，获得铜奖。清华大学排名18，北航排名23，天津大学排名34，浙江大学排名37，北京大学排名43。
				</p>
