                <h2>校友张文庆分享创业经历</h2>

				<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;4月5日，上海交大1985级校友张文庆在上海交大逸夫楼创新创业实验室作主题为“跨越迷茫——多视角下的创业之路回顾与分享”的演讲。创业学院副院长、校团委书记方曦，物理与天文学院党委副书记徐剑波出席本次活动。 
				</p>

				<br/>
 				<p>
					<img src="../img/1025/1.jpg" style="height:70% !important;width:70% !important;" />
				</p>
				<br/>
				<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;讲座开始前，方曦为张文庆校友颁发创业导师证书。 
				</p>
				<br/>
 				<p>
					<img src="../img/1025/2.jpg" style="height:70% !important;width:70% !important;" />
				</p>
				<br/>

				<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;张文庆以大量亲身经历的成功和失败的案例，总结出投资创业项目需要考虑的主要维度和当今新的商业模式。他还结合目前国家对创新创业的扶持政策和区域发展现状，谈论了他所理解的创业的内涵，建议同学们尽快设立自己的人生评价体系，抓住机遇，迎接挑战。
				</p>

				<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;最后，现场的师生就自己感兴趣的问题向张文庆提问，张文庆一一耐心解答，现场气氛活跃。</p> 

				<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;张文庆现任烟台保税港区西区管理局党组书记、局长。近30年来，他先后主持了烟台留学人员创业园区、中俄托姆斯克工贸合作区和烟台开发区投资促进局等工作，对数千个投资项目信息进行搜寻与评估，对300多个各类项目进行策划和投资。</p>

