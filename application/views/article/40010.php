            <h3>分享内容：</h3>
        <p>
            <span>随着电脑计算能力的提升以及硬件价格的降低，消费者已经可以买到体验良好的VR设备。工业界关于VR创业的热潮也是一浪高过一浪。2014年，Facebook以20亿美元收购Oculus Vr公司，则是近年来整个行业发展的标志性事件。MIT科技评论（MIT TR）更是将2016年称为VR最重要的一年。经过长期的深入观察，总结了VR在未来五年里的创新创业的三大机遇，即输入，输出，系统三个方面，并提出其中的若干课题及解决思路。这些与人机交互、计算机图形学、语音识别、计算机视觉、无线网络、深度学习、操作系统等方向密切相关。</span></p>
