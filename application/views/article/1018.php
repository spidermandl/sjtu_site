                <h2>87届计算机系校友指导语音识别课程研发工作</h2>

	         <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2017年4月8日下午，计算机系87届校友返回母校，并指导前沿学科工作。</p>
	            <br/>
 				<p>
					<img src="../img/1018/1.jpg" style="height:70% !important;width:70% !important;" />
				</p>
				<br/>
	         <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;校友们也分别介绍了自己的工作现状，并热烈讨论了未来与计算机系展开产业合作的可能性。今年正值87届校友毕业30周年，旧日同窗久别重逢，感触良多，大家深情追忆了当年在系里的学习与生活，表示希望今后有机会能够尽自己所能，为计算机行业的建设和发展贡献力量。</p>
	         <br/>
 				<p>
					<img src="../img/1018/2.jpg" style="height:70% !important;width:70% !important;" />
				</p>
				<br/>
	 		 <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;座谈过后，校友们参观了智能语音技术实验室和智能计算与智能系统教育部-微软重点实验室，实验室的同学向校友介绍了我系吕宝粮教授和张丽清教授的脑机交互技术和疲劳驾驶检测系统以及俞凯老师的语音识别技术成果。校友们对我系目前取得的成果和学生的素质表示肯定和赞赏，希望未来能与系里进行更多的交流与合作。</p>
