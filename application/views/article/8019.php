                <h3>
                    公司简介
                </h3>
                <br />
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;荟集团队创始成员都曾经长期从事传统媒体行业，尤其在美食、旅游等生活消费领域沉淀多年。专注本地生活服务，为自媒体打造全新的运营服务模式
                </p>
                <br />
                <h3>
                    运营模式
                </h3>
                <br />
                <p>
                    平台理念：
                    <br />
                    <img src="../img/8019/1.png" style="height:auto !important;width:auto !important;" />
                    <br />
                    以 "连接创造价值" 作为经营理念，致力连接2个B端与1个C（商家与自媒体、自媒体与粉丝）的关系，
                    构建移动互联网时代的自媒体营销与交互平台 —— 荟集智惠生活。
                </p>
                <br />
                <p>
                    平台定位：
                    <br />
                    <img src="../img/8019/2.png" style="height:auto !important;width:auto !important;" />
                    <br />
                    荟集智惠生活是自媒体营销与交互平台，目前基于微信生态圈，通过微信企业号， 
                    打通服务号、订阅号与个体，尤其重视联合中小微信公众账号与kol达人，搭建自媒体营销平台， 
                    为旅游、餐饮、酒店等品质生活类行业商家提供一站式新媒体解决方案，为广大用户提供衣食住行资讯与服务。
                </p>
                <br />
                <p>
                    平台模式:
                    <br />
                    <img src="../img/8019/3.png" style="height:auto !important;width:auto !important;" />
                    <br />
                    荟集智惠生活的B2B2C模式，以“轻”面向每一端，以“三轻”建立平台优势。
                    首先，B端-商家 “轻推广”

                    商家与荟集的合作不局限于单一的销售，我们将由专业的BD产品经理根据自媒体及粉丝的特点，联合商家共同定制极具吸引力的高
                    性价比、主题性、体验型的优势产品，并携手合作自媒体共同推广，促进品牌产品的推广与销售。
                    其次，B端-自媒体 “轻运营”

                    对于自媒体而言，独立构建电商系统是一个非常重的运营投入。而在荟集的平台上，我们为自媒体提供技术、产品、客服、物流和
                    参考文案，让自媒体做自己擅长的事情——内容，以更轻松高效的模式实现粉丝价值的转化与营收；
                    最后，C端-消费者“轻消费”

                    基于公众号粉丝的信任基础上，以高性价比的优质体验产品及内容，结合推荐的形式代替繁杂多样的选择，提升消费者的购物体验
                    与选择效率，甚至转发互动、多级传播，以更直接、轻松的方式促成体验型消费的实现。
                </p>
