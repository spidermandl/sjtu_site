				</p>
				<p>&nbsp; &nbsp; &nbsp;&nbsp;据近年来游戏数据报告，中国游戏市场用户数量为6亿人，影响几乎遍及中国当代年轻人群体。而游戏业内从业人员数量、专业能力等各方面，却没有跟上游戏产业发展的步伐。业内人士指出，中国游戏产业人才缺口可达百万。
