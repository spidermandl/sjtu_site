                <h2>Master Engineer和游族网络深入培训合作</h2>

	            <p>&nbsp; &nbsp; &nbsp;&nbsp;Master Engineer 与游族网络内部筹备已久的多个技术系列培训课程将于今夏全面启动，以培养优秀人才、产出精品产品为目的，游族网络尝试搭建“游+地带”，以人才济济的上海为依托，背倚上海各大高校，建立人才储备，贯彻游戏人才前期培养战略规划，提供一个专业的高校人才输出平台。本次合作集中增设游戏Unity3d、Cocos2d、服务器程序开发、美术场景原画、角色原画、数值策划、系统策划等培训课程。
	            </p>
	            <br />
				<p>
