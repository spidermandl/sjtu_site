                <h2>饿了么--未来餐厅</h2>
                <h3>
                    公司简介
                </h3>
                <br />
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;“饿了么”是中国最大的餐饮O2O平台。公司创立于2009年4月，起源于上海交通大学闵行校区。业务主要覆盖全国12个城市，其中包括北京、上海、广州、杭州、苏州、天津、福州、哈尔滨、深圳、南京、长春、厦门。加盟餐厅数共计50000家，2012年完成在线交易规模达到6亿元。目前团队规模达到 200人，秉承“极致、激情、创新”的信仰，致力于推进整个餐饮行业的数字化发展进程。它为用户带来方便快捷订餐体验的同时，也为餐厅提供了一体化的运营解决方案。
                </p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;从2011年3月开始，饿了么先后获得了来自金沙江创投、经纬中国、红杉资本的融资。2014年5月，饿了么获得大众点评8000万美元投资，成为其深度战略合作伙伴。2015年8月28日，饿了么获得由中信产业基金、华联股份领投，华人文化产业基金、歌斐资产等新投资方以及腾讯、京东、红杉资本等原投资方跟投的融资，创下全球外卖平台单笔融资金额的最高纪录，此次融资后的估值超过30亿美元，与美国GrubHub、德国Delivery Hero、英国JustEat一起，成为全球价值最高的外卖巨头。2016年4月13日，阿里巴巴集团投资12.5亿美元入股饿了么，再次打破全球外卖平台单笔融资金额最高纪录。
                </p>
