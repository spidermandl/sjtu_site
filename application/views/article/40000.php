                <h2>2017春节放假安排</h2>
			各位师生员工： 
			<br />

			<p>
				2017春节即将临近，各项工作安排通知如下：
			</p>
			<br />
			<p>
				一、春节期间校区将于1月28日至2月10日关闭学校各个进入出口，不提供任何校内资源。请同学们做好回程安排。
			</p>
			<br />
			<p>
				二、特别要对易发生火险隐患的部位做重点检查。在检查中发现的火险隐患要立即采取措施，予以排除，一时无法排除的，要采取有效应急措施，以确保安全（并报保卫处消防科）。
			</p>
			<br />
			<p>
				三、对在寒假期间有装修、改造工程的楼宇和实验室，各单位值班人员必须对施工单位加强检查和监督，并报保卫处备案。
			</p>
			<br />
			<p>
				四、各院系在放假之前，对各楼、实验室进行一次安全检查，并做好“五关”工作（即：关电、关气、关水、关门、关窗）。
			</p>
			<br />
			<p>
				五、要继续加强对广大师生员工的消防安全教育和管理，杜绝各类违章用电现象的发生（如：违章使用热得快、电热毯、电炉等），严禁乱拉电线和配电间内堆放杂物。
			</p>
			<br />
			<p>
				六、根据公安部有关规定，校园内严禁燃放烟花爆竹。
			</p>
			<br />
			<p>
				为此，保卫处和闵行高校派出所将对单位进行防火安全检查，并将检查的情况汇总，报有关领导。
			</p>
			<br />
			<p style="text-align: right">
			　　特此通知，请相互转告
			</p> 
			<p style="text-align: right">
			　　后勤保障处&nbsp;
			</p>
			<p style="text-align: right">
			　　2016年1月10日&nbsp;
			</p>
