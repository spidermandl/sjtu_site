                <h2>上海猴软信息科技有限公司</h2>
                <h3>
                    公司简介
                </h3>
                <br />
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;上海猴软信息科技有限公司，是一家致力于用移动互联网技术帮助广大企业客户改进业务流程、拓展业务机会、挖掘业务数据价值的高科技公司。公司于2016年初在上海成立，注册资金为100万元。近年来移动互联网的发展势头迅猛，人人都在谈“互联网思维”，但放眼望去，每个行业里真正好的移动信息解决方案少之又少。我们觉得这是一个继电话、互联网之后的又一次信息化革命，如果把我们几位IT从业者的所学所思和宝贵行业经验投入到为国内千千万万的并不精通IT技术的中小企业的移动信息化浪潮中来，这将是一件十分有意义的事情。于是我们发起成立了猴软科技。我们是一家紧跟国际最新技术前沿、技术实力强劲、充满活力和创新精神的软件企业。
                </p>
                <br />
                <h3>
                    核心团队
                </h3>
                <br />
                <p>
