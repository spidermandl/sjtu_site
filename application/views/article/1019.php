                <h2>2017年QS世界大学学科排名发布，上海交通大学计算机科学学科继续跻身全球前50强</h2>

				<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;3月8日，国际高等教育资讯机构Quacquarelli Symonds（QS）在TopUniversities.com上发布了《2017年QS世界大学学科排名》（QS World University Rankings by Subject 2016）。上海交通大学计算机科学学科在本次排名中继续跻身全球前50强，名列第45位。在中国内地大学计算机学科中名列第三，仅次于清华大学（第15名）和北京大学（第16名）。
				</p>
				<br/>
				<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本次排名显示，上海交通大学共有7个学科入围世界前50强，包括机械工程（27）、材料科学（27）、土木工程（32）、电子电气工程（37）、计算机与信息系统（45）、化工（45）、化学（49）。
				本次排名是QS发布的第七版世界大学学科排名，涵盖46个不同学术领域的11000余个位次，是目前为止最大规模也是最复杂的大学排名。QS总计评估了来自全世界74个国家的1127所高校。2017年的新排名涵盖46个学科，比去年多4个，其中包括一些不在国际其他排名中排入的学科。今年的新学科包括：解剖学（Anatomy）、酒店管理与休闲管理（Hospitality and Leisure Management）、体育相关学科、神学等学科。
				</p>
