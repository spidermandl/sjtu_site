				<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;朱健代表学校向张旭豪赠送了捐赠证书及感谢状。朱健代表学校感谢“饿了么”对学校创业工作的支持，要求创业学院要发挥好“饿了么”创业基金在创业孵化、教育、研究等各个环节中的积极作用，推动创业领域相关工作迈上新台阶。朱健同时号召具有丰富创业经验的校友积极回学校分享经验体会，启迪在校学生，提高创新意识和创业素养。朱健强调，创业学院要深入进行规律研究，充分发挥市场活力，通过整合校内外创新创业资源，在“大众创业，万众创新”和上海建设具有全球影响力的科技创新中心的浪潮中努力做出更大贡献。</p>
				<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;拉扎斯网络科技（上海）有限公司（即“饿了么”团队）设立总额为500万元人民币的上海交通大学“饿了么”创业基金。基金主要用于支持上海交通大学创业学院开展孵化、教育、研究，设立“饿了么”创业实习奖学金、举办互联网领域创业大赛和上海交通大学创业训练营等活动。</p>

