                <h2>“饿了么”创业基金签约仪式</h2>

	            <p>&nbsp; &nbsp; &nbsp;&nbsp;4月11日，上海交通大学“饿了么”创业基金签约仪式在闵行校区逸夫楼100号举行。“饿了么”创始人、交大2011届研究生张旭豪，合伙人兼首席战略官、交大2011届研究生康嘉，副总裁、交大2011届本科生罗宇龙，副总裁、交大2009届本科生汪渊等“饿了么”公司员工，校党委副书记、创业学院院长朱健，创业学院常务副院长赵旭，改革与发展研究室主任季波，学指委秘书长林立涛，以及来自团委、创业学院的师生60余人出席仪式。仪式由林立涛主持。</p>
				<p>&nbsp; &nbsp; &nbsp;&nbsp;林立涛介绍了“饿了么”创业历程及“饿了么”创业基金设立背景。“饿了么”从一家初创型公司，成长为全国最大餐饮O2O平台之一，始终秉持“极致、激情、创新”理念七年创业的艰辛，其取得的成就令在校师生倍感振奋；他对“饿了么”公司及张旭豪等校友设立创业基金回馈母校的善举表示感谢。</p>

				<p>&nbsp; &nbsp; &nbsp;&nbsp;赵旭和康嘉分别代表创业学院和“饿了么”公司签署了“上海交通大学‘饿了么’创业基金”备忘录。</p>
				<p>&nbsp; &nbsp; &nbsp;&nbsp;张旭豪代表“饿了么”团队发表演讲，他感谢交大对其个人与“饿了么”团队的培育以及对公司创业初期的支持，他与现场的学弟学妹们分享了自己的创业体会与思考，鼓励大家激发创新创业的意识，充满责任感，充满激情地把握时代机遇，通过积极的努力，去实现自己的梦想。</p>
