                <h2>游族网络股份有限公司</h2>
                <h3>
                    公司简介
                </h3>
                <br />
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;游族网络股份有限公司（SZ.002174）成立于2009年，创始人林奇，是中国领先的轻娱乐供应商。公司总部位于上海，在南京、台北、新加坡等地设有分支机构，立足全球化游戏研发与发行、大数据应用、IP工程管理、泛娱乐产业投资及互联网流量经营五大业务全面发展。2014年6月，游族网络正式登陆A股主板。
                </p>
                <p>  
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;以“分享简单的快乐”为使命，游族网络坚持“大IP”、“大数据”、“全球化”为战略，通过领先的研运优势打造全球发行体系，陆续成功推出了《女神联盟》、《少年三国志》、《万万没想到之大皇帝》等知名游戏产品。从页游到手游，游族网络引领行业创新，以技术突破树立精品标杆，缔造90%自研产品月收入破千万的游族现象。从上海、南京到台北、新加坡，游族分支机构遍布全球八大城市，在海外积累1000多个合作伙伴，发行范围遍及欧美、中东、亚洲及南美等150多个国家及地区，全球化优势显著。
                </p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;与此同时，游族网络还着力于业务模式的持续创新，在IP管理工程指导下首创“影游联动”模式，拿下中国科幻史上第一巨作《三体》版权，打造系列电影、游戏、动漫、小说、商业地产等大文化产品体系，塑造全球经典文化品牌。
                </p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;游族网络坚持内生增长与外延扩张并举，收购全球最大的移动开发者服务平台Mob，覆盖全球28亿移动设备，通过与Google、Facebook及阿里巴巴的战略合作，不断深化大数据优势；同时采用“产业链+孵化+投资”的运营思路布局泛娱乐产业，成立游族创新创业中心，整合上下游产业资源开展全产业链孵化，为早期创业团队提供多维创业服务。未来，游族网络将继续借助资本力量，以大IP为核心聚合资源，以大数据为基础开发产品，打造系列泛娱乐文化产品，成为全球领先的泛娱乐供应商。
                </p>
