                <h2>师大计算机学院领导考察督导教学</h2>

				<p>
				&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;12月22日，师大计算机学院党委书记张立君、就业指导中心主任王明华、计算机学院教学质量科科长张宏哲等一行3来我中心考察交流。我中心接待了考察组一行并进行了座谈会。
				</p>
				<br/>
				<p>
				&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;会上，首先对学院领导的到访表示欢迎，并详细介绍了中心的基本情况。中心技术教学主管孙晓迪博士对中心技术体系架构进行分享，并对未来技术合作提出展望。中心技术顾问余春龙博士对学员学习工作情况作出报告，同时会上邀请部分同学展示学习成果并发表了在企业工作学习的感悟，表现出了对技术工作的强烈热爱和向往。最后师大计算机学院党委书记张立君陈词，代表学院诚挚邀请我中心领导与老师去访问考察，同时表达了希望与我中心保持长期交流的愿望。
				</p>
				<br/>
				<p>
				&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;会后，师大计算机学院领导参观了交大校园，对交大方面所做的工作进行高度评价。
				</p>
