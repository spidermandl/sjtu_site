                <h2>宿舍住宿安全制度强调</h2>
			<p>
				一、外校学生必须遵守学校的规章制度和本校区的住宿规章制度，服从工作人员的管理，爱护宿舍公共财产。
			</p>
			<br />
			<p>
				二、物品要妥善保管，谨防遗失被窃。
			</p>
			<br />
			<p>
				三、学生应按照登记的房间住宿，不得私自转让房间和留客住宿，会客时间不得超过当日二十三时。
			</p>
			<br />
			<p>
				四、严禁将易燃、易爆、腐蚀性、放射性等物品带进宿舍。
			</p>
			<br />
			<p>
				五、保持房间的整洁，爱护房间的设备和物品，如有人为损失、污染、按价赔偿。
			</p>
			<br />
			<p>
				六、不准携带禽、畜等有碍卫生的物品进入宿舍。
			</p>
			<br />
			<p>
				七、严禁私自在房间内使用电饭煲、微波炉、酒精炉、电磁炉、严禁擅自拉电线和安装使用电气设备，严禁在房间内焚烧物品。
			</p>
			<br />
			<p>
				八、严禁在宿舍内进行各种违法犯罪活动，不得赌博、不得张贴、宣传淫秽书画、照片和播放淫秽录像，不准在房间内酗酒、喧闹、影响其他用户。
			</p>
			<br />
			<p>
				九、应主动协助配合宿管搞好治安工作，发现可疑情况及时反映，并接受监督检查。
			</p>
			<br />
			<br />
			<p style="text-align: right">
			　　特此通知，请相互转告
			</p> 
