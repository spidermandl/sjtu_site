                <h2>计算机系密码与计算机安全实验室在国际顶尖密码会议CRYPTO 2015上录用3篇学术论文</h2>

				<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;近日，计算机系特别研究员郁昱、王磊，助理研究员刘志强等密码与计算机安全实验室（简称LoCCS）成员撰写的三篇论文被国际三大密码学术会议之首的美洲密码年会CRYPTO 2015录用。据统计，过去十年间（2005-2014）由国内学者合作发表在CRYPTO的文章共计12篇。此次LoCCS实验室有三篇文章同时被CRYPTO接收，不仅是LoCCS实验室在密码学顶级会议上的历史性突破，同时也是国内学者在密码学研究领域所取得的长足进步。
				</p>
				<br/>
				<p>
				&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;郁昱老师等人发表的论文“(Almost) Optimal Constructions of UOWHFs from 1-to-1, Regular One-way Functions and Beyond”解决了STOC 1989上Naor与Yung提出的基于多类特殊单向函数的单向哈希函数的最优构造问题，是今年CRYPTO大会接收的唯一一篇完全由国内学者合作发表的文章。刘志强老师发表的“Links among Impossible Differential, Integral and Zero Correlation Linear Cryptanalysis”论文则针对不可能差分分析等几种主流的密码分析方法，从理论上构建了它们之间的内在联系，这一结果进一步推动了不同分析方法更细粒度的划分归类工作，从而为分组密码算法的可证明安全研究提供了有力支持，并为算法的设计提供了便利。王磊老师的论文“Known-key Distinguisher on Full PRESENT”首次成功地对最著名的轻量级分组密码算法PRESENT全轮进行了分析，极大地拓展了人们对其实际安全性界限的认知，特别是揭示了其在某些实际应用（比如杂凑函数等）中的安全隐患。
				</p>
				<br/>
				<p>
				&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;这三篇论文的录用是继今年初郁昱老师与博士生吕晨的论文分别被TCC 2015和IEEE TDSC 录用之后，LoCCS实验室在学术研究方面取得的又一佳绩，是学术界对LoCCS实验室在密码领域研究工作的肯定。上海交通大学计算机系LoCCS实验室由谷大武教授领导，主要从事密码学和计算机安全的研究，承担了973计划、863计划、国家科技支撑计划、国家自然科学基金等多项国家级重要科研项目。LoCCS的理论与算法小组主要从事密码基础理论、密码算法设计与分析及抗泄漏密码等方面的科研工作，相关学术成果发表在CRYPTO、TCC、CHES、FSE等密码学的顶级会议上。
				</p>
