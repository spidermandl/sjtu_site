                <h2>上海见行信息科技有限公司</h2>
                <h3>
                    公司简介
                </h3>
                <br />
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;上海见行信息科技有限公司，是一家集行业应用软件开发，销售，服务为一体的专业性软件公司。见行科技一直致力于行业应用软件及IT产业的持续发展，通过与行业各标杆客户长期深入合作，先后推出了适用于医疗等企业成熟的系统软件产品及深层次解决方案。见行公司坚持“客户的口碑是企业的生命”的服务理念，通过先进的技术，让见行科技的产品和服务不断超越用户的期望。
                <p>
                </p>
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;见行科技专注医院数字化后勤，主营即为医院数字化后勤系统，打造医院后勤信息化标杆，提高信息化整体应用水平，提升医院后勤管理形象，建立完善的科学化，信息化，标准化，专业化，规范化后勤管理体系。目前于全国处于领先地位，因为专注，所以专业，将在医院后勤信息化管理之路上渐行渐远！打造成国内数字化后勤领航者！
                </p>
                <br />
                <h3>
                    平台建设
                </h3>
                <br />
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;近年来，随着医疗服务需求和医院管理要求的不断提高，医院信息化建设正处于一个加速发展的时期，全国各大医院对信息化建设的不断重视和加强，纷纷结合自身的特点分别完成了覆盖医疗管理业务的医院信息化系统、电子病历系统、手术麻醉监护系统、影像系统、检验系统和覆盖行政管理工作的办公自动化系统等，提高了医疗工作质量和医院管理水平。
                </p>
                <p>    
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;然而，作为医院重要的支持环节的医院后勤业务却始终处于信息化建设的边缘，成为医院管理的一块“短板”，制约着医院后勤业务的发展与管理水平的提升。
                </p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;因此，如何建立一个完善的后勤管理信息化系统，建设集约型数字化后勤管理平台，以提高医院后勤管理的规范化和科学化，是医院后勤管理和信息管理者共同思考的问题。用信息化的手段对医院后勤进行科学合理的管理将是今后发展的必然趋势。
                </p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;见行科技运用现代信息技术，通过将分散的、孤立的后勤信息汇总处理形成封闭式的信息管理链，从而建立完善的科学化、信息化、标准化、专业化、规范化的 “服务—管理—保障”一体化的后勤管理体系，构建快捷、高效的数字化后勤管理平台，进而规范医院后勤服务流程、提高工作效率、控制运营成本、保证服务质量、辅助管理决策、提升医院后勤整体管理水平。
                </p>
